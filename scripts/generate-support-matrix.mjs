/**
 * Build `docs/SUPPORT_MATRIX.md` from the schema + capability declarations.
 *
 * The component list, attributes, and time-series sheets come from
 * `src/config/pypsa_schema.json` (regenerated from PyPSA master by
 * `generate-pypsa-schema.mjs`). UI / optimisation feature flags come from
 * `src/config/capabilities.json` (hand-edited). This script merges them
 * and produces a single readable markdown table per section.
 *
 * Run via: `node scripts/generate-support-matrix.mjs`
 */
import fs from 'fs/promises';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const ROOT = path.resolve(__dirname, '..');

const SCHEMA_PATH = path.join(ROOT, 'src/config/pypsa_schema.json');
const CAP_PATH = path.join(ROOT, 'src/config/capabilities.json');
const OUT_PATH = path.join(ROOT, 'docs/SUPPORT_MATRIX.md');

function tick(b) { return b ? '✓' : '—'; }

function statusBadge(status) {
  switch (status) {
    case 'supported': return '✓ supported';
    case 'partial':   return '◐ partial';
    case 'planned':   return '○ planned';
    case 'unsupported': return '✗ not supported';
    default:          return status;
  }
}

async function main() {
  const [schemaRaw, capRaw] = await Promise.all([
    fs.readFile(SCHEMA_PATH, 'utf8'),
    fs.readFile(CAP_PATH, 'utf8'),
  ]);
  const schema = JSON.parse(schemaRaw);
  const cap = JSON.parse(capRaw);

  const componentRows = Object.entries(schema.components).map(([sheet, comp]) => {
    const capRow = cap.components[sheet] || { detail_card: false, map_marker: false, notes: '' };
    const inputStatic = comp.input_static_attributes?.length ?? 0;
    const inputTemporal = comp.input_temporal_attributes?.length ?? 0;
    const outputAttrs = comp.output_attributes?.length ?? 0;
    return {
      sheet,
      label: comp.label || comp.sheet_name || sheet,
      inputStatic,
      inputTemporal,
      outputAttrs,
      detailCard: capRow.detail_card,
      mapMarker: capRow.map_marker,
      notes: capRow.notes || '',
    };
  });

  const lines = [];
  lines.push('# Ragnarok — PyPSA support matrix');
  lines.push('');
  lines.push(`> Auto-generated by \`scripts/generate-support-matrix.mjs\` from`);
  lines.push(`> [\`src/config/pypsa_schema.json\`](../src/config/pypsa_schema.json)`);
  lines.push(`> (PyPSA \`${schema.meta?.commit_sha?.slice(0, 7) ?? 'unknown'}\`) and`);
  lines.push(`> [\`src/config/capabilities.json\`](../src/config/capabilities.json).`);
  lines.push(`> **Do not edit by hand** — re-run the script after changing either source.`);
  lines.push('');
  lines.push('## Components');
  lines.push('');
  lines.push('| Component | Sheet | Input static | Input time-series | Outputs | Detail card | Map marker | Notes |');
  lines.push('|---|---|---:|---:|---:|:---:|:---:|---|');
  for (const row of componentRows) {
    lines.push(`| ${row.label} | \`${row.sheet}\` | ${row.inputStatic} | ${row.inputTemporal} | ${row.outputAttrs} | ${tick(row.detailCard)} | ${tick(row.mapMarker)} | ${row.notes} |`);
  }
  lines.push('');
  lines.push('Every component listed above is imported through the generic schema-driven path in `backend/lib/network/__init__.py` — `network` (runtime-import allow-list) and `snapshots` (index special case) are the only exceptions. See [`backend/lib/network/README.md`](../backend/lib/network/README.md).');
  lines.push('');

  lines.push('## Optimisation / scenario features');
  lines.push('');
  lines.push('| Feature | Status |');
  lines.push('|---|---|');
  for (const feat of cap.features) {
    lines.push(`| ${feat.name} | ${statusBadge(feat.status)} |`);
  }
  lines.push('');

  lines.push('## Input / output formats');
  lines.push('');
  lines.push('| Format | Status |');
  lines.push('|---|---|');
  for (const fmt of cap.io) {
    lines.push(`| ${fmt.name} | ${statusBadge(fmt.status)} |`);
  }
  lines.push('');

  await fs.writeFile(OUT_PATH, lines.join('\n') + '\n', 'utf8');
  process.stdout.write(`Wrote ${OUT_PATH}\n`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
